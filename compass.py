def print_all_compass_points():
    for bearing in [north, north_east, east, south_east,
                    south, south_west, west, north_west]:
        print(bearing)


def choose_compass_point(θ):
    θ %= 360
    bearings = {0: east, 45: north_east, 90: north, 135: north_west,
                180: west, 225: south_west, 270: south, 315: south_east}

    return bearings[min(bearings.keys(), key=lambda x: abs(x - θ))]


